// ──────────────────────────────────────────────
// Compatible type derivation
// ──────────────────────────────────────────────
// Initiative & Expression → complementary (opposites attract)
// Distance & Values → same (need to align)
export function getMatchCode(userCode) {
  const [init, expr, dist, val] = userCode.split('');
  return [
    init === 'L' ? 'F' : 'L',
    expr === 'W' ? 'A' : 'W',
    dist,
    val,
  ].join('');
}

// Calculate user code from axis scores
export function calculateCode(scores) {
  return [
    scores.initiative >= 0 ? 'L' : 'F',
    scores.expression >= 0 ? 'W' : 'A',
    scores.distance >= 0 ? 'C' : 'S',
    scores.values >= 0 ? 'P' : 'G',
  ].join('');
}

// ──────────────────────────────────────────────
// Compatibility check between two match types
// ──────────────────────────────────────────────
export function getCompatibility(typeA, typeB) {
  let score = 0;
  // Initiative & Expression: complementary (different) is good
  if (typeA[0] !== typeB[0]) score += 25;
  if (typeA[1] !== typeB[1]) score += 25;
  // Distance & Values: same is good
  if (typeA[2] === typeB[2]) score += 25;
  if (typeA[3] === typeB[3]) score += 25;
  return score;
}

